dtm <- as.matrix(DocumentTermMatrix(VCorpus(VectorSource(omnibus$text_cleaned))))
texts <- data.frame(Terms = colnames(dtm), Freq = colSums(dtm), row.names = NULL)
texts$Prop <- round(texts$Freq/sum(texts$Freq),7)

relevance <- function(text){
  df <- as.data.frame(strsplit(text, " "))
  colnames(df) <- "Terms"
  df <- df %>% left_join(texts)
  df <- na.omit(df)
  return(sum(df$Prop))
}

toBinary <-  function(x) {
  x <-  ifelse(x > 0, 1, 0)
  x <-  factor(x, levels = c(0, 1))
  return (x)
}

